#include <stdio.h>
int main()
{
    signed int i,j;           /*定义有符号基本整型变量*/
    i=-2147483648;            /*为变量进行赋值*/
    j=2147483647;             /*为变量进行赋值*/
    printf("有符号整型变量最小值是%d\n",i);      /*显示整型变量*/
    printf("有符号整型变量最大值是%d\n",j);      /*显示整型变量*/
	return 0;                                    /*程序结束*/
}