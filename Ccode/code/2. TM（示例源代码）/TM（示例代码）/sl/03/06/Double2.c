#include<stdio.h>
int main()
{
    double d,h,v,res;                 /*定义一个双精度类型变量*/
     d=50;                            /*对变量进行赋值，d表示直径，h表示高，v表示体积*/
     h=15;
     v=3.14*(d/2)*(d/2)*h;                   /*根据体积公式计算结果*/
     printf("该粮仓体积为%f立方米\n",v);     /*显示结果*/
     res=896*v;                               /*计算存储粮食结果*/
     printf("该粮仓一共可储存%f千克粮食\n",res);   /*显示结果*/
	 return 0;                                     /*程序结束*/
}