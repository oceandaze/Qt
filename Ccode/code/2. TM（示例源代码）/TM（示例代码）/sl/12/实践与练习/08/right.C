#include<stdio.h>
void main()
{
	int x=50,y=-50;
	x=x>>3; 									/*x右移3位*/
	y=y>>3; 									/*y右移3位*/
	printf(" & & & & & & & & & & & & & & & & & & & & &\n");
	printf(" &    50、-50右移3位的结果分别为:%d,%d    &\n",x,y);
	x=x>>2; 									/*x右移2位*/
	y=y>>2; 									/*x右移2位*/
	printf(" &  再此基础上又右移2位的结果分别为:%d,%d &\n",x,y);
	printf(" & & & & & & & & & & & & & & & & & & & & &\n");
}
